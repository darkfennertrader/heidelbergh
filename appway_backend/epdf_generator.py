def generate_epdf_dcm(
    job_id: str,
    input_dir: Path,
    output_path: Path,
    inference_result: Optional[dict] = None,
    png_dirs: Optional[list[Path]] = None,
) -> None:
    """
    Generate a DICOM Encapsulated PDF result report for MyopicCNV+.

    - Reads all .dcm files in input_dir for metadata
    - Builds an A4 PDF report via the pixel-faithful template-overlay
      generator (``appway_backend.pdf_report.build_pdf``)
    - Wraps it in a DICOM ePDF object
    - Copies the AppWay credential block from the first input DICOM
    - Saves to output_path

    Args:
        job_id:           unique job identifier
        input_dir:        directory with the original .dcm files
        output_path:      destination for the result.dcm
        inference_result: optional dict returned by inference.run_inference()
                          with keys verdict, processing_time, per_image.
        png_dirs:         optional list of directories (one per input DICOM)
                          where the processor dumped the extracted PNG frames.
                          Used by the gallery renderer to load positive images
                          and stamp a red bounding box + confidence chip.
    """
    dcm_files = sorted(input_dir.rglob("*.dcm"))
    if not dcm_files:
        raise ValueError(f"No .dcm files found in {input_dir}")

    now = datetime.now(timezone.utc)
    date_str = now.strftime("%Y%m%d")
    time_str = now.strftime("%H%M%S")

    # ── Collect metadata ──
    input_infos, donor_ds = _collect_input_info(dcm_files)

    # ── Build PDF ──
    pdf_bytes = _build_pdf_bytes(
        job_id, input_infos, now,
        inference_result=inference_result,
        png_dirs=png_dirs,
    )
    logger.info("[%s] Generated PDF: %d bytes", job_id, len(pdf_bytes))

    # ── Build DICOM dataset ──
    # DocumentTitle and SeriesDescription are VR LO (max 64 chars).
    # "MyopicCNV+ Result for Job <uuid>" = 68 chars → truncate label to fit.
    short_title = f"{SOLUTION_NAME} Result {job_id}"[:64]
    ds = _base_dicom_dataset(donor_ds, date_str, time_str)
    ds.SeriesDescription = short_title
    ds.ImageComments = f"Result from {SOLUTION_NAME}"
    ds.DocumentTitle = short_title
    ds.MIMETypeOfEncapsulatedDocument = "application/pdf"
    ds.EncapsulatedDocument = pdf_bytes
    ds.ConceptNameCodeSequence = Sequence([])

    _attach_referenced_inputs(ds, input_infos)

    # ── Copy AppWay credential block (0011,10xx) — mandatory for routing ──
    # ⚠️  See _copy_credential_block doc-comment for the strict no-other-
    # private-tags rule.
    _copy_credential_block(ds, donor_ds, job_id)

    # ── Save ──
    output_path.parent.mkdir(parents=True, exist_ok=True)
    ds.save_as(str(output_path), implicit_vr=False, little_endian=True)
    logger.info("[%s] ePDF DICOM saved → %s  (%d bytes)", job_id, output_path, output_path.stat().st_size)


def generate_error_epdf_dcm(
    job_id: str,
    input_dir: Path,
    output_path: Path,
    error_message: str,
) -> None:
    """
    Generate a DICOM Encapsulated PDF *error* result for a failed job.

    Same DICOM structure as ``generate_epdf_dcm()`` but the encapsulated
    PDF contains a plain-text error description instead of real analysis
    results.

    Best-effort:
      - Tries to read input DICOMs to preserve patient/study tags + credential block.
      - If the input directory is empty or unreadable, emits a minimal ePDF with
        no donor metadata — the credential block will be absent (meaning the
        outbound AppWay Link may not be able to route it back, but at least
        there is a persistent record in S3 and operators get the SNS alert).
    """
    now = datetime.now(timezone.utc)
    date_str = now.strftime("%Y%m%d")
    time_str = now.strftime("%H%M%S")

    # ── Best-effort read of input DICOMs for donor metadata ──
    donor_ds: Optional[Dataset] = None
    input_infos: list[dict] = []
    try:
        dcm_files = sorted(input_dir.rglob("*.dcm")) if input_dir.exists() else []
        if dcm_files:
            input_infos, donor_ds = _collect_input_info(dcm_files)
    except Exception:
        logger.exception("[%s] Could not read input DICOMs while building error ePDF", job_id)

    # ── Build error PDF ──
    pdf_bytes = _build_error_pdf_bytes(job_id, error_message, now)
    logger.info("[%s] Generated ERROR PDF: %d bytes", job_id, len(pdf_bytes))

    # ── Build DICOM dataset ──
    # DocumentTitle and SeriesDescription are VR LO (max 64 chars).
    short_err_title = f"{SOLUTION_NAME} ERROR {job_id}"[:64]
    ds = _base_dicom_dataset(donor_ds, date_str, time_str)
    ds.SeriesDescription = short_err_title
    ds.ImageComments = f"ERROR result from {SOLUTION_NAME}: {(error_message or '')[:200]}"
    ds.DocumentTitle = short_err_title
    ds.MIMETypeOfEncapsulatedDocument = "application/pdf"
    ds.EncapsulatedDocument = pdf_bytes
    ds.ConceptNameCodeSequence = Sequence([])

    _attach_referenced_inputs(ds, input_infos)

    # ── Copy credential block ──
    if donor_ds is not None:
        _copy_credential_block(ds, donor_ds, job_id)
    else:
        logger.warning(
            "[%s] No donor DICOM available — error ePDF will NOT have credential block. "
            "AppWay Link may not route it back.", job_id,
        )

    # ── Save ──
    output_path.parent.mkdir(parents=True, exist_ok=True)
    ds.save_as(str(output_path), implicit_vr=False, little_endian=True)
    logger.info(
        "[%s] ERROR ePDF DICOM saved → %s  (%d bytes)",
        job_id, output_path, output_path.stat().st_size,
    )
